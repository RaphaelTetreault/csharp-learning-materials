using System;

class Program
{
    static void Main()
    {
        // We can add numbers together using the '+' sign
        Console.WriteLine($"The sum of 5 and 12 is '{5 + 12}'");

        // We can assign the sum of numbers to a variable
        int sum = 9 + 90;
        Console.WriteLine($"The sum of 9 and 90 is '{sum}'");

        // We can also add a lot of numbers together at once.
        sum = 1 + 20 + 300 + 4000;
        Console.WriteLine($"The sum of 1, 20, 300, and 4000 is '{sum}'");
    }
}

